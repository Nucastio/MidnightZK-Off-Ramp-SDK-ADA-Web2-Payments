#!/usr/bin/env node
import { createHash } from "node:crypto";
import { existsSync, readFileSync, readdirSync, statSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const sdkDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const repoDir = path.resolve(sdkDir, "..");
const contractDir = path.join(repoDir, "contract", "src");
const managedDir = path.join(contractDir, "managed", "offramp");
const outputPath = path.join(sdkDir, "src", "midnight", "artifact-manifest.generated.ts");

function filesIn(relativeDir, extensions) {
  const absoluteDir = path.join(managedDir, relativeDir);
  return readdirSync(absoluteDir)
    .filter((name) => extensions.some((extension) => name.endsWith(extension)))
    .sort()
    .map((name) => ({
      absolutePath: path.join(absoluteDir, name),
      runtimePath: path.posix.join(relativeDir, name),
    }));
}

const artifactPaths = [
  { absolutePath: path.join(contractDir, "offramp.compact") },
  { absolutePath: path.join(managedDir, "compiler", "contract-info.json"), runtimePath: "compiler/contract-info.json" },
  { absolutePath: path.join(managedDir, "contract", "index.js"), runtimePath: "contract/index.js" },
  ...filesIn("keys", [".prover", ".verifier"]),
  ...filesIn("zkir", [".bzkir", ".zkir"]),
];

for (const artifact of artifactPaths) {
  if (!existsSync(artifact.absolutePath)) {
    throw new Error(`Missing committed Midnight artifact: ${artifact.absolutePath}`);
  }
}

const artifacts = artifactPaths.map(({ absolutePath, runtimePath }) => {
  const bytes = readFileSync(absolutePath);
  return {
    path: path.relative(repoDir, absolutePath).split(path.sep).join("/"),
    ...(runtimePath === undefined ? {} : { runtimePath }),
    sha256: createHash("sha256").update(bytes).digest("hex"),
    size: statSync(absolutePath).size,
  };
});

const manifest = {
  version: 1,
  contractId: "offramp",
  circuits: [
    "bindOffRampIntent",
    "proveAmountBinding",
    "proveComplianceFlag",
    "proveOffRampSettlement",
    "provePayeeBinding",
  ],
  artifacts,
};
const canonicalManifest = JSON.stringify(manifest);
const manifestHash = createHash("sha256")
  .update("offramp:midnight-artifact-manifest:v1\n", "utf8")
  .update(canonicalManifest, "utf8")
  .digest("hex");

const generated = `/* Generated by sdk/scripts/generate-artifact-manifest.mjs. Do not edit manually. */\nexport const OFFRAMP_ARTIFACT_MANIFEST = ${JSON.stringify(manifest, null, 2)} as const;\n\nexport const OFFRAMP_ARTIFACT_MANIFEST_HASH = ${JSON.stringify(manifestHash)};\n`;

if (process.argv.includes("--check")) {
  const current = existsSync(outputPath) ? readFileSync(outputPath, "utf8") : "";
  if (current !== generated) {
    console.error(`Artifact manifest is stale: ${outputPath}`);
    process.exit(1);
  }
} else {
  writeFileSync(outputPath, generated, "utf8");
}
